#!/usr/bin/env node

import { execSync } from "node:child_process";
import { existsSync, readFileSync, renameSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dist = join(root, "dist");

function resolveCommitSha() {
  if (process.env.GIT_COMMIT_SHA?.trim()) return process.env.GIT_COMMIT_SHA.trim();
  if (process.env.GITHUB_SHA?.trim()) return process.env.GITHUB_SHA.trim();
  try {
    return execSync("git rev-parse HEAD", { cwd: root, encoding: "utf8" }).trim();
  } catch {
    return "unknown";
  }
}

function assertExists(path, label) {
  if (!existsSync(path)) {
    throw new Error(`${label} not found at ${path}. Run vite build first.`);
  }
}

const shellRenames = [
  ["ecosystem.html", "ecosystem-shell.html"],
  ["cockpit.html", "operator-shell.html"],
  ["auth.html", "auth-shell.html"],
  ["council.html", "council-shell.html"],
];

for (const [source, target] of shellRenames) {
  assertExists(join(dist, source), source);
  renameSync(join(dist, source), join(dist, target));
}

const ecosystemShell = readFileSync(join(dist, "ecosystem-shell.html"), "utf8");
writeFileSync(join(dist, "index.html"), ecosystemShell);

if (!existsSync(join(dist, "app", "index.html"))) {
  console.error("assemble-operator-dist: dist/app/index.html missing after storefront assembly");
  process.exit(1);
}

const pkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const commitSha = resolveCommitSha();
const buildTimestamp = process.env.BUILD_TIMESTAMP?.trim() || new Date().toISOString();

const manifest = {
  commitSha,
  buildTimestamp,
  appVersion: pkg.version,
  environment: "build",
  assembledAt: new Date().toISOString(),
  source: "scripts/assemble-operator-dist.mjs",
  shells: shellRenames.map(([, target]) => `/${target}`),
};

writeFileSync(join(dist, ".build-manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`);

const workerBuildInfo = `// Generated by scripts/assemble-operator-dist.mjs — do not edit manually.
export const BUNDLED_BUILD_COMMIT_SHA = ${JSON.stringify(commitSha)};
export const BUNDLED_BUILD_TIMESTAMP = ${JSON.stringify(buildTimestamp)};
export const BUNDLED_APP_VERSION = ${JSON.stringify(pkg.version)};
`;

writeFileSync(join(root, "worker", "bundledBuildInfo.ts"), workerBuildInfo);

console.log("Operator dist assembled:", manifest);
